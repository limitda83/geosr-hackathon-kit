def region_label(info: RegionInfo) -> str:
    """집계용 정규화 지역명: '경상남도 통영시'."""
    return f"{info.sido} {info.sigungu}"


def build_alias_index() -> list[tuple[str, str]]:
    """(별칭, 내부키) 목록을 별칭 길이 내림차순으로 — 긴 표면형 우선 매칭."""
    pairs: list[tuple[str, str]] = []
    for key, info in GAZETTEER.items():
        for alias in info.aliases:
            pairs.append((alias, key))
    pairs.sort(key=lambda p: len(p[0]), reverse=True)
    return pairs
